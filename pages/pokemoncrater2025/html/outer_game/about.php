<h2 style="text-align: center;">About Us / FAQ</h2>
<div style="width: 95%; margin: 0 auto; text-align: left;">
<p><span class="itemHeader">What is the Pok&eacute;mon Vortex Battle Arena?</span><br />
The Pok&eacute;mon Vortex Battle Arena is an online game where you can catch your own online Pok&eacute;mon, battle with them, trade them, and interact with other trainers.</p>

<p><span class="itemHeader">How much does it cost?</span><br />
The game has no cost, it is fully supported by advertising services.</p>

<p><span class="itemHeader">Will my personal information be viewable to other players?</span><br />
Only if you want it to be. Your email and other information not directly related to the game is hidden from other members by default, but you may choose for them to be shown. Personal information such as your e-mail address are required by us for account security purposes.</p>

<p><span class="itemHeader">What are the basics I need to know to play the game?</span><br />
When you sign up, you'll choose a starter Pok&eacute;mon. To catch more Pok&eacute;mon, simply move around on one of the maps until another one appears. You can battle this Pok&eacute;mon and try to capture it. If you capture it, it will be added to your team and you can use it to battle other people and catch more Pok&eacute;mon!<br />If your team is full, it will be placed in your Pok&eacute;mon storage.</p>

<p><span class="itemHeader">When you try to login, you keep getting a message that your session has expired.</span><br />
This site uses cookies to login to the arena. When the 'Session Expired' page always appears when you try to login, this means that cookies are not enabled on your browser. Change your cookie settings to accept cookies and this problem should go away. This error could also mean that we have temporarily put the game down for maintenance.</p>

<p><span class="itemHeader">What's the difference between Normal, Shiny, Dark, Metallic, Shadow and Mystic Pok&eacute;mon?</span><br />
Shiny Pok&eacute;mon have 25% more HP than Normal Pok&eacute;mon and Dark Pok&eacute;mon do 25% more damage than Normal Pok&eacute;mon. Mystic Pok&eacute;mon have a chance to scare the defending Pok&eacute;mon whenever they attack (scaring is another status effect). Metallic Pok&eacute;mon have 25% more defence than Normal Pokemon and Shadow Pok&eacute;mon have immunity to status effects such as Sleep (Not yet added)</p>

<p><span class="itemHeader">How do I catch legendary Pok&eacute;mon?</span><br />
Legendary Pok&eacute;mon will appear randomly on the maps, only after you have defeated all gym leaders, elite 4's, champions, Hoenn/Sinnoh battle frontier's and the Battle Maison. (This does not include Events or Sidequests).</p>

<p><span class="itemHeader">Where is (Pok&eacute;mon name) found?</span><br />
All Pok&eacute;mon appear randomly on the maps. However, some Pok&eacute;mon only appear on certain maps, (e.g. some Pok&eacute;mon are found only in the caves maps while others are found only in the grass maps) and water Pok&eacute;mon are usually only found in the water. My best advice is to use common sense. For example, You would find Geodude in a cave map. Certain Pok&eacute;mon will also only appear in the day or night - You can change your time preference in the Options tab found on the side of every page. For the most part, on maps you will find only low leveled base form pokemon, but on occasion you may encounter legendary pok&eacute;mon as well as evolved pok&eacute;mon at higher levels. <a href="#" target="_BLANK">Click here for the official Vortex location guide.</a></p>

<p><span class="itemHeader">How do you switch Pok&eacute;mon in your team with a Pok&eacute;mon in your Pok&eacute;mon box?</span><br />
Go to the "Your Pok&eacute;mon Team" page, there is a link above your team that says "Change the Pok&eacute;mon in your team", which lets you switch Pok&eacute;mon. But remember, you need at least 7 Pok&eacute;mon in order to switch. You can't trade or release Pok&eacute;mon that are in your party, only those that are in your box. If you do not have at least 7 Pok&eacute;mon, you will have to catch more so you will able to switch them out of your party.</p>

<p><span class="itemHeader">How do I get on the Top 100 Trainers list?</span><br />
It takes a lot of time and patience to get on the top trainers.  You need to have a lot of high level Pok&eacute;mon with a lot of experience, along with a high battle count to get on the top 100.</p>

<p><span class="itemHeader">I forgot my password! What can I do?</span><br />
Passwords can unfortunately not be retrieved. We advise keeping a note of your password for Pok&eacute;mon Vortex so you don't forget it and avoid such problems.</p>

<p><span class="itemHeader">Can I battle my friends and how do I do it?</span><br />
In version 3 it is currently not possible to live battle your friends, however you can play a computer controlled battle against your friends account, also, live battles will be implimented in a later version so you can battle against friends in real time.</p>

<p><span class="itemHeader">Can I battle other people when they are not online?</span><br />
You can battle people who aren't online by going to the "Battle Any Member" page. However, their Pok&eacute;mon will be computer controlled.</p>

<p><span class="itemHeader">What are the stones (ie: Moon Stone, Water Stone, Thunder Stone, etc.)?</span><br />
These stones are for evolving some of the Pok&eacute;mon that require a stone to evolve. Before you evolve them, you will need to buy the appropriate stone and it will be used when you evolve that Pok&eacute;mon.</p>

<p><span class="itemHeader">What do the points mean?</span><br />
The points are calculated from your unique pok&eacute;mon count, total experience, average experience, and total battles won. They are the same points as are displayed on the top 50 trainers list and are meant to show your relative progress. Don't be discouraged if they are really low, many of the people on the top trainers list have been here for a long time.</p>

<p><span class="itemHeader">Someone contacted me saying they were staff of Vortex and want my password or want me to change my email. Should I do it?</span><br />
Absolutely not, the staff of Pok&eacute;mon Vortex do not need your password under any circumstance, so don't believe anyone who asks for your password regardless of who they are. If you change your email to someone else's you are giving them the opportunity to use the password retrieval system to get your password, so don't do it.</p>

<p><span class="itemHeader">I've found a glitch in the game, what should I do about it?</span><br />
You should report it as soon as you can to one of our staff. Anyone found abusing a glitch of some beneficial nature will be subject to a ban.</p>
