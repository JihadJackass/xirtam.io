<h2 style="text-align: center;">Pok&eacute;mon  Battle Arena v1.8</h2>
<center><p>Just to let you all know, we haven't closed the game for a reason you won't like.<br />
Over the past months, we have been working on a new version for you all, v1.8.<br />
We couldn't quite make it v2 due to the lack of live battles, but what we've done will be just as enjoyable for you all.<br />
There are countless new features we've created for you to do.<br />
While we implement them and test them all before opening the game back up, here is a list for you to look through to see what will be added once re-opening .</center><p>
<div style="width: 95%; margin: 0 auto; text-align: left;">
<b>Gyms and Badges:</b><br />
We have updated to every possible gym and gym like feature from previous Pokemon games and anime's.<br />
All Unova gym leaders (5th gen)<br />
Orange Island gym leaders<br />
Champions from Kanto, Johto, Hoenn, Sinnoh and Unova<br />
The Hoenn battle frontier<br />
The Sinnoh battle frontier<br />
 gym leader (You can battle the admins from here on , Patrick, Rob and Dean).<p>
<b>Events:</b><br />
We have now made full use of the events section for profiles by adding a variety of new obtainable event badges through event completion. Some are permanent, some are for a limited time only. They will also be updated with new ones when a suitable event is available.<br />
Team Rocket - Rocket Badge<br />
Team Aqua - Aqua Badge<br />
Team Magma - Magma Badge<br />
Team Galactic - Galactic Badge<br />
Team Plasma - Plasma Badge<br />
 Admins - Admin badge.
Cipher - Colosseum Badge<p>

We have updated a few current features such as the navigation menu bars including a 'Community' tab which provides pop-out links to;  Chat,  Forums, , Facebook and  Twitter. In addition we have took the top trainers list from the members tab and made it an all new length to top 100, giving all you newer members a better chance of 'becoming the best' and being able to monitor your rank. We have also now fully automated the  lottery which will be drawn every Friday depending on your time zone.<p>
The trade system has been fully redone, to increase speed and reduce confusion. You have numerous options of what you would like to do once you enter the trade page including see your Pokemon for trade, put Pokemon for trade, Pokemon you have offered in trade and recent trade notifications. Pokemon you have up for trade have now been grouped into pages with 50 Pokemon per page. You can also order your Pokemon up for trade now simply by clicking a column option such as offers, name or level.<p>
We've ripped and added in new map sprites for you to choose from for a wider variety. A total of 25 more have been added. Along side this is special trainer sprites on the maps to match their elemental set map where you can battle them to train your Pokemon, these trainers all have level 100 Pokemon to maximise the experience given at the end.<p>
For the two above features, we have included all new map designs for the Grass, Fire, Cave and Electric maps making them bigger and more appealing. In total there are 9 new Grass maps, 6 new Cave maps, 2 new Electric maps and 4 new Fire maps, each one as before with their elemental Pokemon on.<p>
The water maps no longer exist, instead you can find the water Pokemon in the water on the grass maps and in the water on the cave maps.<br />
It is still in discussion whether to add new water maps.<br />
The maps also come with another extended feature where you can now tell more obviously if you have the Pokemon encountered. The name will now be followed by a Pokeball if you have the Pokemon and as before, in bold. If not the Pokemon's name will be displayed as usual.<p>
Small addition, but the Pokedex tab has now been added into the layout of the site for those of you who open it into a new browser window for referal.<br />

Achievements are now available through certain milestones of your account. You will receive a ribbon added to your in-game profile for each achievement you complete. We will always be looking to find new achievements to be added to keep you motivated through game play, trying to achieve a goal.<br />
<b>Here is a detailed list of each achievement:</b><br />
-1,000 Battles Won (One Thousand Battles Completed)<br />
-10,000 Battles Won (Ten Thousand Battles Completed)<br />
-100,000 Battles Won (One Hundred Thousand Battles Completed)<br />
-1,000,000 Battles Won (One Million Battles Completed)<br />
-1,000 Unique's (One thousand unique Pokemon obtained and kept on your account)<br />
-2,000 Unique's (Two thousand unique Pokemon obtained and kept on your account)<br />
-3,650 Unique's (Three thousand six hundred and fifty Pokemon obtained and kept on your account)
<i>(If you remove the Pokemon, you will lose the badge)</i><br />
-Won the lottery (You must have won the lottery once on that specific account)<br />
-All Gyms, Elite 4 and Champions (Complete all gyms, elite 4 and champions from every region)<br />
-Hoenn Frontier Brain (Defeat all of the Hoenn battle frontier)<br />
-Sinnoh Frontier Brain (Defeat all of the Sinnoh battle frontier)<br />
-Kanto Sidequests (Complete all of the Kanto sidequests)<p>

Onto the biggest addition of the version, Sidequests.<br />
Sidequests comes as it's own side game of  where you can battle through each region from various versions of the Pokemon games and Anime.<br />
There are 14 sets of sidequests in total which will eventually calculate to over 1000 battles to complete to win badges and prizes for your account.<br />
Sidequests works in a way you can't re-battle an opponent to ensure people can't repeat prizes but this isn't fully the case. In the Options tab, you can reset your sidequests to redo them, this will set you right back to the very first battle of Kanto sidequests.<br />
As far as this version is, only Kanto of sidequests is done but I will be adding more regions very soon and hopefully have them all completed within version 1.8.<p>

As well as all these updates there is the appealing new layout you see, the blue layout. For those of you who feel blue isn't your colour then there is the option to set it back to red which you can find in the Options tab or on your profile with the other options.<p>

Finally, the items and attack pages, you can now purchase Pokemon from the item page, there is only 5 Pokemon in the shop but will change every so often, it will include 3 random Shiny, Dark, Mystic or Ancient Pokemon, 1 random Shiny, Dark, Mystic or Ancient legendary Pokemon and 1 random Shiny, Dark, Mystic or Ancient legendary form Pokemon.<br />
If you buy one of these Pokemon or want to change any other Pokemon's attacks, you can now choose from over 350 attacks set and all lay out in an easier to understand way, all organized in order of alphabetical type.<p>

This version is not complete and is still set for updates where possible, but here is the beginning of this whole new feel to the site.<br />
We hope you enjoy it to it's full potential and appreciate the hard work gone into bringing you all this in such a short time.<br />
